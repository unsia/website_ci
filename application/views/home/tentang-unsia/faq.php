                    prinsip pendirian dari Kemenristek DIKTI pada 26
                    Agustus 2019 pada Hari Kebangkitan Teknologi
                    Nasional, di Bali. Universitas Siber Asia ini adalah
                    universitas swasta pertama yang dipimpin oleh rektor
                    asing asal Korea yaitu Prof. Jan Youn Cho, Ph.D.,
                    MPA., CPA.
                  </p>
                  <p style="text-align: justify">
                    Universitas Siber Asia akan menjalankan tiga
                    strategi utama, yaitu meningkatkan kuantitas,
                    memberikan fitur-fitur pengajaran yang sesuai dengan
                    masa depan era revolusi industri 4.0 dan
                    menghadirkan pengajaran dengan kualitas dunia (world
                    class learning).
                  </p>
                  <p style="text-align: justify">
                    Pendidikan berbasis full online dan full
                    learning management system ini juga selaras dengan
                    kebijakan pemerintah dan menjawab tantangan Era
                    Revolusi Industri 4.0, memberikan efisiensi dalam
                    layanan pendidikan tinggi dalam skala tidak terbatas
                    bagi penduduk Indonesia tidak saja di perkotaan
                    namun juga daerah terpencil yang memiliki akses
                    internet. Bahkan dengan biaya pendidikan yang
                    terjangkau, dapat membantu para pekerja dengan
                    penghasilan rendah untuk tetap dapat mengakses
                    pendidikan tinggi.
                  </p>
                  <p style="text-align: justify">
                    Nantinya, sistem pembelajaran Universitas Siber Asia
                    memanfaatkan jaringan internet secara terbuka dan
                    masif melalui program Massive Open Online Course
                    akan menghadirkan fitur-fitur pembelajaran
                    berorientasi masa depan untuk mempersiapkan
                    lulusannya menghadapi era revolusi industri 4.0.
                  </p>
                  <p style="text-align: justify">
                    Universitas Siber Asia juga akan menjaga kualitas
                    dengan membawa pembelajaran kelas dunia yang salah
                    satunya dilakukan dengan membawa para pengajar
                    terbaik dari berbagai negara seperti Amerika, Korea
                    dan juga Indonesia. Didukung dengan insfrastruktur
                    dan teknologi dari Korea, serta pengajar yang
                    kompeten di bidangnya baik dari dalam negeri maupun
                    luar negeri, Universitas Siber Asia ditargetkan akan
                    mulai menerima mahasiswa baru tahun depan melalui
                    lima program studi yang akan dibuka yaitu Manajemen, Akuntansi, Sistem Informasi, Informatika, Komunikasi.
