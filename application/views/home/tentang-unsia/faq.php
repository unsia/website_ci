                  </a>
                </h6>
              </div>
              <div id="faq1" class="acod-body collapse">
                <div class="acod-content">
                  Online learning disebut juga e-Learning atau Online
                  Course adalah implementasi pendidikan yang
                  memanfaatkan media Teknologi Informasi dan Komunikasi
                  dalam proses belajar mengajar. Dalam perkuliahan
                  online atau kuliah non tatap muka ini mahasiswa tidak
                  perlu duduk dengan manis di ruang kelas untuk menyimak
                  penjelasan seorang dosen secara langsung. Namun,
                  mahasiswa cukup membuka media pembelajaran, yakni
                  Learning Management System (LMS) yang telah disediakan
                  oleh UNSIA. Kuliah Online juga merupakan salah satu
                  sarana pembelajaran interaktif, dosen dan mahasiswa
                  dapat berkomunikasi anytime, anyplace, bahkan pada
                  saat hari libur nasional. Selain itu di LMS dosen dan
                  mahasiswa dapat melaksanakan video conference sebagai
                  fasilitas live streaming. Dengan cara tersebut,
                  komunikasi dosen dengan mahasiswa akan terjalin
                  interaktif dan diskusi bisa menghasilkan produktifitas
                  pengajaran yang bermutu. Dosen dapat memberikan materi
                  kuliah, baik berupa file, video, maupun tulisan
                  (teks). Dengan kuliah online, seorang dosen juga bisa
                  mengajar di beberapa tempat secara bersamaan.
                </div>
              </div>
