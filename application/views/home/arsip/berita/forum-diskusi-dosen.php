							<h5 class="post-title"><a href="#">Dampak Omnibus Law Bagi Masa Depan Penyiaran
									Indonesia</a></h5>
							<p>Jakarta – Kamis (05/11/2020) Program Studi Ilmu Komunikasi Universitas Nasional (UNAS)
								bekerjasama dengan Program Studi Komunikasi Universitas Siber Asia (UNSIA) dan Komisi
								Penyiaran Indonesia Daerah (KPID) Provinsi DKI Jakarta mengadakan webinar bertajuk
								Forum Diskusi Komunikasi (FDK) yang mengusung tema “Dampak Omnibus Law Bagi
								Masa Depan Penyiaran Indonesia”. Bertujuan sebagai wadah yang merefleksikan berbagai
								perspektif para akademisi komunikasi dalam menyikapi dinamika dan perubahan, termasuk
								perkembangan regulasi dunia penyiaran di Indonesia.</p>
							<p>FDK kelima ini dihadiri sebanyak 250 peserta dari kalangan mahasiswa Prodi Ilmu
								Komunikasi UNAS dan Prodi Komunikasi UNSIA, para praktisi dan umum, dengan
								menggandeng Media partner, diantaranya; UNAS TV, UNAS Radio, UNSIA TV, Radio
								Frambors, dan Radio Elshinta. Acara ini dipandu oleh Moderator Rosanah, M.I.Kom.,
								AMIPR, selaku Ketua Program Studi Komunikasi UNSIA.</p>
							<p>Sebagai pembuka, sambutan dari Ketua Program Studi Ilmu Komunikasi UNAS yang
								diwakilkan oleh Nursatyo, M.Si. Pada sambutannya, Sekretaris Program Studi Ilmu
								Komunikasi UNAS tersebut menyampaikan bahwa tema yang diangkat dalam webinar kali
								ini sangat aktual yang menimbulkan berbagai macam dinamika. Acara ini diharapkan dapat
								menstimulus terhadap kontribusi aktif dari berbagai kalangan khususnya dalam mencermati
								terkait regulasi penyiaran Indonesia.</p>
							<p>Sambutan kedua, disampaikan oleh Agung Suprio sebagai Ketua KPI Pusat. Suprio
								menyampaikan bahwa Undang-undang (UU) Omnibus Law Nomor 32 terdapat berbagai
								perdebatan mengenai masa depan penyiaran, “Apakah Omnibus Law ini mengambil sebagian
								kewenangan KPI dalam mengawasi penyiaran?</p>
							<p>Lebih lanjut, suprio menjelaskan dalam perjalanannya UU mengenai penyiaran mengalami
								beberapa perubahan. Pertama di tahun 2004 redaksi mengenai penyiaran berbunyi “disusun
								oleh KPI bersama Pemerintah”. Namun demikian Mahkamah Konstitusi (MK) merubah frase
								tersebut menjadi “disusun oleh Pemerintah” tanpa melibatkan KPI di dalamnya. Hal
								tersebut
								merupakan hasil gugatan yang dilayangkan ke MK yang bersifat final dan mengikat. Kedua,
								terkait dengan kepemilikan atau oligarki penyiaran, dalam UU Penyiran No.32 sangat jelas
								ada larangan kepemilikan media TV dalam jumlah banyak.</p>
							<p>“Televisi di Indonesia masih berbasis analog. Artinya Televisi sebagai suatu produk atau
								alat
								dan sebagai sistem yang terkait dengan frekuensi. Pada Tahun 2022 mendatang, Televisi
								akan
								mengalami perpindahan dan perubahan menjadi digital sistem. Digital disini dapat
								dimaknai
								sebagai perkembangan kualitas penyiaran Tv seperti lebih jernih, frekuensi lebih hemat
								dan
								terakhir akan melahirkan Tv-Tv digital dengan berbagai macam konten. “Saya berfikir tema
								ini menjadi menarik, pertama dapat memberikan pencerahan terkait proses regulasi
								penyiaran, kedua mengenai peran KPI setelah terbitnya Omnibus Law dan ketiga, mengenai
								partisipasi publik terhadap penyiaran” pungkas Suprio.</p>
							<p>Webinar penyiaran ini menghadirkan narasumber Th. Bambang Pamungkas, M.I.Kom,
								selaku Kominsioner KPID Provinsi DKI Jakarta yang juga merupakan Dosen Program Studi
								Ilmu Komunikasi UNAS. Dalam pemaparannya, Bambang menjelaskan penyiaran di
								Indonesia memasuki paradigma baru mengenai digitalisasi yang terdapat pada UU cipta
								kerja
								(Omnibus Law). Dengan disahkan UU cipta kerja ini kita ucapkan “selamat dalang era
								digitalisasi”, artinya payung hukum mengenai hal ini sudah mendapat pengesahan resmi
								dari
								pemerintah. Perubahan ini (digitalisasi) akan berdampak pada perkembangan jaringan
								internet berbasis 5.0, yaitu akses digital akan lebih mudah, lebih cepat dan lebih
								murah.</p>
							<p>“Memasuki era revolusi 5.0 yang ditandai dengan maraknya media masa yang tumbuh
								berkembang bagaikan “jamur dimusim hujan”. UU Nomor 11 tahun 2020 tentang cipta kerja
								dibidang penyiaran merupakan suatu terobosan sendiri dalam agenda utama terkait
								digitalisasi. Dalam ayat 1 dijelaskan bahwa “Penyelenggaraan penyiaran dilaksanakan
								dengan mengikuti perkembangan teknologi, termasuk migrasi penyiaran dari teknologi
								analog ke teknologi digital”. Artinya mau tidak mau, suka tidak suka, penyiaran harus
								masuk
								ke ruang digitalisasi, kita dituntut untuk memasuki wilayah digitalisasi”, tandas
								Bambang.</p>
							<p>Diakhir presentasi, Bambang menegaskan bahwa digitalisasi merupakan sebuah dampak
								positif dari UU cipta kerja, bahwa pemerintah sangat komitmen dan serius untuk segera
								memasuki era digitalisasi. Rekomendasi yang muncul terkait hal ini yaitu pemerintah
								harus
								melakukan uji publik sebagai upaya tidak terjadinya dominasi tehadap suatu pihak atas
								berbagai kepentingan. Oleh Karena itu, KPID berkomitmen dan mengajak seluruh
								masyarakat berpartisipasi aktif dalam mengawal pelaksanaan penyiaran demi kepentingan
								publik. (prodikom).</p>
