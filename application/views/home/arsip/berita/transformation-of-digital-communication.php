								alt="">
							<div class="text-center">
								<small>Dr. Muhammad Sulhan, S.IP., M.Si. – Ketua Umum Asosiasi Pendidikan Tinggi Ilmu
									Komunikasi (ASPIKOM)</small></div>
							<br>
							<p>Guna mengimbangi kedua narasumber yang berasal dari birokrasi dan dunia bisnis,
								narasumber ketiga justru menghadirkan Dr. Muhamad Sulhan, S.IP., M.Si dengan pemaparan
								tema “Transformasi Komunikasi Digital Perspektif Teoritis”. Sulhan menyampaikan bahwa
								transformasi komunikasi ke dalam ranah digital saat ini memiliki berbagai problem di
								dalamnya. Namun demikian, khalayak sebagai pengguna (user) justru menikmatinya dengan
								mencontohkan seperti rasa kecanduan dalam bermain telepon genggam (handphone)
								mengalahkan kecanduan terhadap minuman. Ini semua diawali dengan hadirnya Facebook
								hanya sebatas berbicara masalah pertemanan, Twitter yang berbicara masalah opini, dan
								Instagram yang berbicara masalah pengalaman. Namun, fenomena baru pun muncul yaitu
								hadirnya TikTok yang berbicara masalah kesenangan (having fun).</p>
							<p>Sulhan menggarisbawahi hal yang paling unik dari hal digital adalah tentang budaya.
								Jantung
								budaya digital terbagi menjadi interaksi sosial dan jejaring sosial. Dunia digital
								menjadi
								menarik manakala teknologi menjadi aktor sosial dalam kehidupan manusia. Peran dari
								“orang” komunikasi dalam perspektif dunia digital adalah bagaimana memandang medianya.
								Output yang dihasilkan pun berupa kajian mengenai budaya media digital, sehingga timbul
								suatu istilah “jalan tol” dunia komunikasi saat ini bernama dunia digital yang bersifat
								cepat,
								rahasia, dan nyaman. Dalam konteks ini information society dan digital society merupakan
								dua unsur besar yang akan dihadapi oleh generasi saat ini. Di masa depan media menjadi
								perantara dan jembatan utama dalam segala hal aktivitas komunikasi yang terkait dengan
								informasi dan digitalisasi. Maka kajian kembali kepada media, yang memegang kunci
								permainan mencakup khalayak (netizen), bahasa, dan pesan.</b>
								<p>Peserta yang hadir dalam acara webinar kali ini tidak hanya melibatkan mahasiswa baru
									Program Studi Komunikasi UNSIA, melainkan juga mahasiswa Universitas Nasional
									(UNAS), kalangan akademisi, dan masyarakat umum. Tercatat sebanyak 275 peserta hadir
									dan mengikuti seminar baik melalui zoom meeting maupun live streaming di kanal
									Youtube
									“UnsiaTv”.</p>
								<p>Ketua Program Studi Komunikasi UNSIA, Rosanah, S.S., M.I.Kom., AMIPR menuturkan,
									webinar perdana ini merupakan upaya Program Studi Komunikasi UNSIA dalam menyambut
									baik mahasiswa baru. Sebagai stimulus yang diharapkan dapat memberikan manfaat dan
									inspirasi kepada seluruh mahasiswa dalam merespon transformasi komunikasi digital.
									Kedepannya, Rosanah berharap acara webinar semacam ini dapat terus dilakukan secara
									berkala serasa berharap mendapat respons positif dari berbagai pihak guna eksplorasi
									berbagai fenomena dan dinamika komunikasi yang terjadi dari para pakar di bidangnya,
									terutama Bidang Penyiaran Digital, Komunikasi Digital dan Komunikasi Perusahaan.
									(prodikom) </p>
