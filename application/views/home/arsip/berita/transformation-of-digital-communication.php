							<h5 class="post-title"><a href="#">“Transformation of Digital Communication” Menjadi Tema
									Webinar Perdana
									Prodi Komunikasi UNSIA</a></h5>
							<p>Jakarta – Sabtu (31/10/20) Program Studi Komunikasi Universitas Siber Asia (UNSIA)
								menyelenggarakan Web-Seminar (Webinar) edisi perdana dengan mengusung tema
								“Transformation of Digital Communication”, sebagai refleksi terhadap berbagai perubahan
								dan tantangan bidang komunikasi di era Revolusi Industri 4.0. Melalui tema ini, webinar
								ini
								diharapkan dapat memberikan pandangan luas dalam memahami perkembangan dan
								transformasi komunikasi digital, khususnya bagi mahasiswa baru Program Studi Komunikasi
								UNSIA.</p>
							<p>Seminar berbasis Online ini dimulai pada pukul 10.00 WIB ini dibuka oleh sambutan dari
								Ir.
								Abdul Wahab Bangkona, M.Sc selaku Wakil Rektor Bidang Adm. Umum, Keuangan, SDM
								dan Kerjasama Universitas Siber Asia (UNSIA). Dalam sambutannya, Wahab menyampaikan
								bahwa acara virtual seminar ini merupakan sesuatu yang sangat strategis, penting, dan
								mendesak karena teknologi berkembang maju dalam segala aspek kehidupan manusia.
								Terlebih komunikasi merupakan sebuah pilar kehidupan khususnya bagi generasi penerus dan
								sangat menentukan sukses tidaknya sebuah kelompok sosial di tengah masyarakat.</p>
							<p>“Harus ada kesadaran secara nasional mengenai transformasi komunikasi. Terlebih lagi
								generasi saat ini sudah memasuki era digital sehingga harus ada peningkatan pemahaman
								dan
								kesadaran mengenai standar, kaidah, dan etika dalam berkomunikasi yang berorientasi pada
								hasil (output) yang maksimal dalam konteks kemajuan bersama. Webinar ini pun diharapkan
								dapat berkontribusi mengenai pengembangan regulasi-regulasi nasional dalam ranah
								komunikasi sehingga seluruh lapisan masyarakat bisa berkomunikasi dengan skill yang
								memadai”. Tandas Wahab yang pernah menjabat sebagai Sekretaris Jenderal
								Kemenakertrans RI.</p>
							<p>Webinar ini menghadirkan 3 (tiga) narasumber dari kalangan praktisi dan akademisi, yaitu
								Agung Suprio selaku Ketua KPI pusat, Agung Laksamana M.Sc, selaku Ketua Umum
								Perhumas, dan Dr. Muhamad Sulhan, S.IP., M.Si, selaku Ketua Umum Aspikom. Dipandu
								oleh Moderator Dosen Senior Ilmu Komunikasi Universitas Nasional (UNAS) Jakarta, Nieke
								Monica Kulsum, S,E., M.Si. Seminar ini dibagi dalam tiga sesi, yaitu satu sesi bagi
								setiap
								pembicara untuk memaparkan materi dan disambung dengan diskusi serta tanya jawab.</p>
							<img src="#" class="lazy" data-src="<?= base_url(); ?>assets/images/berita/agungsuprio.jpeg"
								alt="">
							<div class="text-center">
								<small>Agung Suprio – Ketua Komisi Penyiaran Indonesia (KPI) Pusat</small></div>
							<br>
							<p>Narasumber pertama, Agung Suprio menyampaikan materi mengenai “Tantangan Regulasi
								Penyiaran Digital dan Media Baru”. Dalam pemaparannya, Suprio menjelaskan bahwa di
								Asia Tenggara Indonesia masih sama kedudukannya dengan Timor Leste dalam hal
								transformasi digital. Artinya, Indonesia masih tertinggal dari negara lain karena belum
								melakukan Analog Switch Off (ASO) yaitu perpindahan atau migrasi ke digital frekuensi
								televisi di Indonesia.</p>
							<p>“Regulasi yang berkaitan dengan Undang-Undang Cipta Kerja (Omnibus Law). Dalam UU
								Cipta Kerja dikatakan bahwa 2 tahun mendatang Indonesia akan melakukan transformasi
								digital (Analog Switch Off). Televisi analog akan melakukan perpindahan ke digital,
								begitu
								pun dengan internet yang akan mengalami transformasi dari 4G ke 5G. Oleh karena itu, KPI
								harus membuat regulasi yang seimbang karena selama ini KPI identik dengan pengawasan
								terhadap siaran/tayangan televisi. Maka hal ini harus menjadi catatan tersendiri bagi
								KPI
								dalam merancang regulasi mengenai media baru yang menjadi sarana konsumsi masyarakat”
								Ungkap Suprio.</p>
							<img src="#" class="lazy"
								data-src="<?= base_url(); ?>assets/images/berita/agunglaksamana.jpeg" alt="">
							<div class="text-center">
								<small>Agung Laksamana, M.Sc., IAPR – Ketua Umum Perhimpunan Hubungan Masyarakat
									Indonesia (Perhumas)</small></div>
							<br>
							<p>Ketua Umum Persatuan Hubungan Masyarakat Indonesia (PERHUMAS), Agung
								Laksamana, M.Sc, memaparkan materi yang kedua dengan tema “Transformasi Public
								Relations (PR) di Era Digital”. Peran Public Relations atau Humas di era transformasi
								digital
								menjadi topik utama, yaitu membedah proses komunikasi dalam menyampaikan pesan
								perusahaan untuk menumbuhkan trust (kepercayaan) kepada para stakeholders. Terdapat
								beberapa poin penting yang menjadi tantangan dunia PR di era digital, seperti aspek
								government relations yang berkaitan dengan regulasi, maupun dalam aspek komunikasi
								digital yang menjadi bagian dari ranah publik.</p>
							<p>“Suatu perusahaan harus memiliki sikap kehati-hatian di segala lini dalam
								mengomunikasikan pesan kepada publik. Keseluruhan peran PR dalam menghadapi suatu
								perubahan dapat dipahami melalui formula “Right Conten–Right People–Right Time–Right
								Channel–Personalization”. Pada akhirnya, terdapat sebuah “mantra PR” yang meliputi
								ADOPT yang berarti pintar mengadopsi situasi dan kondisi saat ini, ADAPT yang berarti
								pintar beradaptasi dengan strategi baru/inovatif/kolaboratif, dan ADEPT yang berarti
								mahir
								(expert) di era transformasi digital saat ini”, tambah Laksamana.</p>
