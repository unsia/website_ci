									<h5 class="post-title"><a href="#">Tips Belajar Online yang Nyaman</a></h5>
									<p>Apakah di sekolah atau kampusmu sudah menerapkan sistem belajar online? Pernahkah kamu menerka bagaimana caranya supaya belajar melalui komputer itu dapat berjalan dengan baik dan nyaman?</p>
									<p>Sejak hadirnya COVID 19 di Indonesia, begitu banyak institusi pendidikan yang kini menerapkan Pembelajaran Jarak Jauh yang tak bisa dilepaskan dari pembelajaran berbasis online . Dalam sistem ini, kamu tidak perlu hadir tatap muka bersama dengan dosen. Kamu bisa belajar dari mana pun dan kapanpun.</p>
									<p>Agar kamu bisa nyaman belajar, berikut ini tips yang bisa kamu lakukan.</p>
									<p>
										<ul class="ml-4">
											<li>
												Siapkan pakaian yang rapih <br>
												Tidak jarang dalam kegiatan belajar online, pengajar baik itu guru ataupun dosen meminta Video Conference  Nah, tidak mau kan saat di zoom meeting pakaian yang kita pakai terlihat tidak rapih? Anggaplah ketika kamu akan mulai mengikuti kelas online sama seperti ketika kamu mengikuti perkuliahan yang biasa.
											</li>
											<li>Siapkan Spot khusus untuk belajar <br>
											Entah kamu menggunakan smartphone ataupun Desktop Komputer, sebaiknya ada satu ruangan yang bisa kamu pakai untuk belajar. Ruangan ini dapat kamu anggap sebagai ruang kelas kamu sebagaimana jam pelajaran yang biasa.
										</li>
											<li>Fokuskan pikiran <br>
											Ketika mengikuti perkuliahan online sebaiknya kamu benar-benar memfokuskan pikiran. Anggaplah bahwa jam belajar online sama seperti kamu belajar secara konvensional. Matikan notifikasi dari aplikasi-aplikasi lain.
										</li>
											<li>Kuasai Penggunaan Learning Management System yang disediakan kampus <br>
											Dalam pembelajaran online, mahasiswa tidak akan bertemu tatap muka baik dengan dosen maupun dengan rekan-rekan yang lain di ruang kelas. Dalam Pembelajaran Online pengganti dari ruang kelas konvensional dikenal sebagai Learning Management System. Di sana sistem perkuliahan akan berjalan; mulai dari mengisi daftar hadir kuliah, menyaksikan video pembelajaran, membaca materi pelajaran, mengikuti ujian dan lain sebagainya dilakukan di sini.
										</li>
										</ul>
									</p>
									<p>Begitulah tips belajar online yang bisa kamu praktikan agar belajar mu semakin nyaman.</p>
