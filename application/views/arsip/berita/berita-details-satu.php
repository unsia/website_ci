							<h5 class="post-title"><a href="#">Resmikan Universitas Siber Pertama, Wapres Tekankan
									Pentingnya Unsia Dorong Apk Indonesia</a></h5>
							<p>Jakarta, 22 September 2020 – Wakil Presiden Republik Indonesia, Prof. Dr. KH Ma’ruf Amin
								meresmikan Universitas Siber Asia (UNSIA) secara daring, Selasa (22/9). UNSIA merupakan
								universitas siber pertama di Indonesia yang berada di bawah naungan Yayasan Memajukan
								Ilmu dan Kebudayaan (YMIK) dan telah mendapat izin Kementerian Pendidikan dan
								Kebudayaan. Peresmian dilakukan Wapres dengan menandatangani prasasti UNSIA yang turut
								disaksikan oleh Duta Besar Republik Korea untuk ASEAN Country, Mr Lim Sungnam dan
								Menteri Pendidikan dan Kebudayaan, Nadiem Anwar Makarim.</p>
							<p>Ma’ruf menegaskan hadirnya UNSIA dapat memberikan dampak positif bagi pengembangan
								pendidikan di Indonesia sebagai negara kepulauan dengan kondisi sosial ekonomi yang
								beragam. Pendidikan melalui sistem pembelajaran daring atau e-learning, lanjutnya, dapat
								menjadi sebuah pilihan bagi masyarakat untuk mengakses pendidikan tinggi. Saat ini,
								imbuhnya, baru 20 dari 4.771 perguruan tinggi di Indonesia yang menerapkan pembelajaran
								daring atau e-learning. Ma’ruf pun berharap semakin banyak Lembaga Pendidikan yang
								membuka sistem pembelajaran daring, sehingga semakin banyak kesempatan bagi masyarakat
								untuk melanjutkan Pendidikan tinggi.</p>
							<p>Kehadiran UNSIA, lanjut Ma’ruf, menjadi penting mengingat Angka Partisipasi Kasar (APK)
								perguruan tinggi di Indonesia pada tahun 2019, menurut BPS masih sekitar 30, 28%.
								Artinya, lanjut dia, dibandingkan dengan jumlah penduduk usia 19 – 23 tahun, masyarakat
								yang melanjutkan pendidikan ke jenjang pendidikan tinggi hanya sekitar 30%. Sedangkan
								menurut angka survei angkatan kerja nasional (sakernas) BPS pada februari 2020 angkatan
								kerja kita yang berjumlah 137, 91 juta, hanya 14, 2 juta lulusan PT. Atau hanya 10,3%
								yang berkesempatan menikmati pendidikan tinggi.</p>
							<p>‘’Dengan adanya Universitas Siber Asia ini saya berharap angka-angka tersebut akan terus
								meningkat sehingga masyarakat yang dapat menikmati pendidikan tinggi akan terus
								bertambah sejalan dengan upaya pemerintah menempatkan pembangunan SDM unggul sebagai
								prioritas nasional,’’ tegasnya.</p>
							<p>Hal senada juga turut disampaikan oleh Ketua Yayasan Memajukan Ilmu dan Kebudayaan
								(YMIK), Ramlan Siregar. Ramlan mengatakan lahirnya UNSIA dapat memberikan layanan
								pendidikan tinggi dalam skala yang sangat luas bagi seluruh bangsa Indonesia secara
								efisien dan efektif, serta berkualitas dengan biaya terjangkau.</p>
							<p>“Universitas Siber Asia didirikan selain untuk menjawab tantangan zaman di era revolusi
								industri 4.0 juga mengusung semangat untuk meningkatkan Angka Partisipasi Kasar (APK)
								Indonesia agar usia produktif yang melanjutkan ke jenjang Pendidikan tinggi dapat
								meningkat dan sejajar tidak hanya dengan negara tetangga namun juga dengan negara-negara
								maju lainnya”, tambah Ramlan.</p>
							<p>Menteri Pendidikan dan Kebudayaan, Nadiem Anwar Makarim turut menyambut gembira lahirnya
								UNSIA sebagai perguruan tinggi yang sepenuhnya daring. Ia pun menyampaikan harapannya
								kepada UNSIA agar dapat melahirkan lulusan yang kompeten.</p>
							<p>‘’Saya berharap Unsia menjadi penyelenggara pendidikan jarak jauh yang berkualitas. Tidak
								hanya membangun hard skills, tapi juga soft skills mahasiswanya. Itulah tantangan dari
								pembelajaran yang sepenuhnya daring. Yaitu, bagaimana mencetak sarjana yang kompeten dan
								unggul secara holistic,’’ ungkap Nadiem dalam sambutannya secara daring.</p>
							<p>Nadiem juga mengapresiasi komitmen UNSIA untuk mendukung kebijakan Merdeka Belajar dan
								mendorong agar UNSIA dapat mengirimkan mahasiswa dan dosen ke luar kampus sebanyak
								banyaknya.</p>
							<p>‘’Hadirkan sebanyak mungkin praktisi ke dalam kampus. Ini sangat penting, agar lulusan
								UNSIA betul-betul siap kerja. Tidak hanya menguasai teori, tapi juga bisa berkontribusi.
								Yang terpenting, agar lulusan UNSIA merdeka dalam berpikir. Selalu belajar sepanjang
								hayat. Itu lah inti dari Merdeka Belajar,’’ tuturnya. </p>
							<b>Rektor Pertama asal Korea Selatan</b>
							<p>UNSIA dipimpin oleh Rektor pertama yang berasal dari Korea Selatan, Jang Youn Cho. Rektor
								Cho meneghabiskan 17 tahun di Amerika Serikat untuk belajar dan mengajar, dimana dirinya
								menjadi professor di University of Nebraska – Lincoln. Sekembalinya di Korea, ia
								mengajar selama 20 tahun dan memimpin Business School dan pengalaman memimpin
								universitas siber di Hankuk Cyber University, korea selatan.</p>
							<p>Dengan pengalaman tersebut, Cho bersama Rektor UNAS, Dr. El Amry Bermawi Putera dibawah
								Yayasan Memajukan Ilmu dan Kebudayaan telah merintis pendirian Universitas Siber Asia
								sejak 4 tahun lalu yang diinisiasi dengan kerjasama Internasional yang telah terjalin
								antara UNAS dengan Cyber Hankuk University di Korea Selatan. Kolaborasi antara Indoensia
								dengan Korea ini diharapkan dapat memberikan akses Pendidikan tinggi yang lebih luas
								kepada seluruh masyarakat Indonesia melalui online platform.</p>
							<p>“Kami ingin berkontribusi pada pendidikan tinggi dengan memberikan pendidikan berkualitas
								kepada banyak pemuda yang tidak memiliki kesempatan untuk kuliah di universitas”, ungkap
								Cho.</p>
							<p>Duta Besar Republik Korea untuk Negara ASEAN, Lim Sungnam dalam sambutannya menyampaikan
								bahwa Universitas Siber Asia sejalan dengan kerjasama bilateral yang dirintis oleh
								Indonesia dan Korea dan merupakan output pertama di bidang Pendidikan tinggi. Hadirnya
								pendidikan tinggi online ini akan meningkatkan kesempatan generasi muda Indonesia untuk
								mendapatkan masa depan yang lebih baik. (*humas)</p>
