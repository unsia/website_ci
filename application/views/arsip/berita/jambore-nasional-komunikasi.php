							<h5 class="post-title"><a href="#">“Mahasiswa Prodi Komunikasi UNSIA Raih Prestasi Gemilang
									di Jambore Nasional
									Komunikasi (JNK) ASPIKOM Tahun 2020</a></h5>
							<p>Jakarta – Rabu (16/12/2020) Mahasiswa Program Studi Komunikasi Universitas Siber Asia
								(UNSIA) untuk pertama kali berpartisipasi dalam ajang Jambore Nasional Komunikasi (JNK)
								tahun 2020 yang diselenggarakan oleh Asosiasi Pendidikan Tinggi Komunikasi (ASPIKOM)
								Koordinator Wilayah (Korwil) Jabodetabek, dan berhasil meraih sembilan (9) Juara pada
								cabang Lomba Film Dokumenter.</p>
							<p>Prestasi ini diumumkan oleh panitia penyelenggara saat acara puncak JNK pada 16 Desember
								2020 lalu yang bertajuk webinar nasional dengan tema “Adaptasi dan inovasi Menghadapi
								Tantangan Komunikasi Pasca Pandemi Covid-19” dengan Narasumber dari berbagai latar
								belakang akademisi dan praktisi bidang komunikasi, yaitu: Prof. Dr. Ibnu Hamad selaku
								Guru Besar Universitas Indonesia, Prof. Dr. Widodo Muktiyo selaku Dirjen Informasi dan
								Komunikasi Publik Kementerian Kominfo RI, Makroen Sanjaya selaku Direktur TVMU dan
								Kristy Nelwan yang merupakan Head of Communication Unilever Indonesia.</p>
							<p>JNK ASPIKOM Jabodetabek tahun 2020 ini merupakan tahun ketiga, dimana
								penyelenggaraan pada tahun ini dalam masa adaptasi kebiasaan baru pasca pandemi Covid19.

								Meski demikian, panitia JNK 2020 sukses mengadakan serangkaian acara dan kegiatan
								lomba bidang komunikasi. JNK dibuka dengan webinar “Tata Kelola Perguruan Tinggi
								Menuju Kampus Merdeka Pada Situasi Pandemi”, pada 28 Agustus 2020 lalu dimana
								menghadirkan Narasumber Rektor Universitas Bina Nusantara, Rektor Universitas
								Gunadarma dan Rektor Universitas Mercu Buana.</p>
							<p>JNK ASPIKOM tahun 2020 ini mengadakan 8 (delapan) cabang lomba bidang komunikasi
								yang diperlombakan, meliputi: Lomba new Anchor & Presenter, Lomba Jurnalistik Fotografi,
								Lomba Penulisan Opini, Lomba Iklan Layanan Masyarakat, Lomba Film Dokumenter,
								Lomba Program Issue & Crisist Management, Lomba Program Komunikasi Publik, dan
								Lomba Dosen Teladan. UNSIA menjadi salah satu kampus peserta yang mengirimkan
								sebanyak 12 film karya mahasiswa untuk dikompetisikan pada cabang lomba film
								dokumenter yang diselenggarakan oleh Universitas Nasional (UNAS). UNSIA berhasil
								meraih berbagai kategori, diantaranya: </p>

							<ol class="ml-4">
								<li>Juara Pertama Kategori Film Dokumenter Artistik dan Dekorasi, berjudul “Pelayanan
									Publik di Masa Pandemi Covid-19”, karya Fardian Antariksa, dkk.</li>
								<li>Juara Kedua Kategori Film Dokumenter Artistik dan Dekorasi berjudul “Sekolah
									Pandemi Anak Usia Dini”, karya Abdullah Mufid Mubarok, dkk. </li>
								<li>Juara Kedua Kategori Cinematography dan Special Effect Terbaik, berjudul “Bersama
									Covid-19, Hidup Kami Tidak Tergilas”, karya Hantoro Wibowo, dkk.</li>
								<li>Juara Harapan Kedua Kategori Cinematography dan Special Effect Terbaik, berjudul
									“Gebrakan!”, karya Andreas Eko Soponyono, dkk.</li>
								<li>Juara Kedua Kategori Film Dokumenter Ide dan Konsep Terbaik, berjudul “Bersama
									Covid-19, Hidup Kami Tidak Tergilas”, karya Hantoro Wibowo, dkk.</li>
								<li>Juara Harapan Kedua Kategori Film Dokumenter Ide dan Konsep Terbaik, berjudul
									“No, This is Not End”, karya Ramadhani Safira G, dkk.</li>
								<li>Juara Kedua Kategori Film Dokumenter Terbaik, berjudul “Bersama Covid-19, Hidup
									Kami Tidak Tergilas”, karya Hantoro Wibowo, dkk.</li>
								<li>Juara Harapan Kedua Kategori Film Dokumenter Terbaik, berjudul “No, This is Not
									End”, karya Ramadhani Safira G, dkk. </li>
								<li>Juara Harapan Pertama Kategori Film Dokumenter Terfavorit, berjudul “Antara
									Masyarakat, Covid-19 dan New Normal”, karya Ahmad Faisah, dkk. </li>
							</ol>

							<p>Prestasi perdana yang ditorehkan oleh mahasiswa Prodi Komunikasi UNSIA pada ajang JNK
								2020 ini tentu saja menjadi suatu kebanggaan tersendiri khususnya bagi Prodi Komunikasi
								terlebih bagi Universitas Siber Asia (UNSIA), sebagai universitas siber swasta yang baru
								diresmikan oleh Wapres Ma’ruf Amin pada 22 September 2020 Lalu. Sambutan hangat dan
								apresiasi yang setinggi-tingginya diberikan oleh Kaprodi Komunikasi UNSIA, Rosanah,
								S.S.,
								M.I.Kom atas berbagai prestasi yang diraih oleh seluruh pemenang lomba JNK ASPIKOM
								2020 khususnya dari kalangan mahasiswa Prodi Komunikasi UNSIA. Rosa berharap, semoga
								ajang JNK ASPIKOM 2020 ini menjadi suatu momentum yang baik dan positif dalam
								menstimulusi seluruh mahasiswa Komunikasi UNSIA untuk selalu berkarya dan
								berkontribusi aktif dalam berbagai lomba lainnya sebagai wadah pengembangan bidang
								keilmuan Komunikasi, sekaligus berharap semoga dapat menginspirasi dan memotivasi
								seluruh mahasiswa untuk berpartisipasi serta meningkatkan prestasi pada semua cabang
								lomba JNK ASPIKOM pada tahun mendatang. (Prodikom) .</p>

